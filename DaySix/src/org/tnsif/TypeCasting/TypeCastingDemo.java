package org.tnsif.TypeCasting;
//Type casting
public class TypeCastingDemo {

	public static void main(String[] args) {
		//Implicit Type Casting
		int x=20;
		float y=x;
		System.out.println("Y is Float "+y);
		
		//Explicit Type Casting
		double d=13.7;
		long l=(long)d;
		System.out.println("d is long : "+l);
		
	}

}
